void ruby_draw_text(char *text, unsigned short count, unsigned short x, unsigned short y, unsigned short fgcolor, unsigned short bgcolor)
{
#ifdef RUBY_VAR_TEXT
#define FONT_HEIGHT  14
#define FIRSTCHAR	0x20
		int line, width, cc, bitnum;
		unsigned short bits;
		int ystart = y;
		int xstart = x;
		unsigned int ch;
		
		/* traverse string */
		for(cc = 0; cc < count; cc++) {
				ch = (unsigned int)*(text + cc);
			/* get character width */
			width = _ruby_font_width[ch - FIRSTCHAR];
			//printk(KERN_DEBUG "char :%c int val: %x width:%d offset : %d\n", *(text+cc), ch, width, _ruby_font_offset[ch - FIRSTCHAR]);
			for(line = 0; line < FONT_HEIGHT; line++){
				bits = _ruby_font_bits[ _ruby_font_offset[ch - FIRSTCHAR] + line];
				for(bitnum = 0; bitnum < width; bitnum++)
					if((0x8000 >>bitnum) & bits) ruby_draw_pixel( xstart-line, ystart+bitnum, fgcolor);
			}
			/* calculate next character start position */
			ystart += width;
		}
#else
	
	int xstart = x; /* font height */
	int ystart = y; /* increment font width for each char */
	unsigned short val = 0;
	int i, ind;
	//printk("ruby_draw_text: %s\n", text);
	for(ind = 0; (ind < count) && (ind < 22); ind++) {
		//printk(KERN_ERR "Char val : %x - %c\n", *(text+ind), *(text+ind));
		lcd_set_display_window(xstart - 19, ystart, xstart, ystart + 10);
		lcdif_write_reg_single(0x06);
		for( i = 0; i < (10 * 20); i++) {
			val = (_10x20bitmaps[*(text+ind)][i] > 0)?fgcolor:bgcolor;
			lcdif_write_data_single(val >> 8);
			lcdif_write_data_single(val & 0xff);
		}
		ystart += 10;
	}

#endif
}
